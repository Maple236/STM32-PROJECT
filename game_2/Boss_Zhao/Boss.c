void Boss_Init(Boss_t* boss, int16_t x, int16_t y, int16_t type) {
    boss->x = x;
    boss->y = y;
    boss->direction = 1;
    boss->last_attack_tick = HAL_GetTick();

    boss->laser_fire_tick = 0;
    boss->laser_warning_tick = 0;
    boss->laser_vertical = 0;

    boss->type = type;

    if (type == 1) {
        boss->width = 80;
        boss->height = 80;
        boss->speed = 2;
        boss->hp = 10;
        boss->max_hp = 10;
        boss->attack_interval_ms = 1000;
    }
    else {
        boss->width = 80;
        boss->height = 80;
        boss->speed = 1;
        boss->hp = 10;
        boss->max_hp = 10;
        boss->attack_interval_ms = 800;
    }
}

void Boss_TakeDamage(Boss_t* boss) {
    if (boss->hp > 0) {
        boss->hp = boss->hp - 1;
    }
}

int Boss_Alive(Boss_t* boss) {
    return boss->hp;
}

void Boss_Update(Boss_t* boss, BulletPool_t* boss_bullets, Player_t* player) {

    if (boss->type == 1) {
        boss->y += boss->speed * boss->direction;

        if (boss->y <= 0) {
            boss->direction = +1;
        }
        if (boss->y >= SCREEN_HEIGHT - boss->height) {
            boss->direction = -1;
        }
    }
    else {
        if (boss->x < player->x) {
            boss->x = boss->x + boss->speed;
        }
        if (boss->x > player->x) {
            boss->x = boss->x - boss->speed;
        }
        if (boss->y < player->y) {
            boss->y = boss->y + boss->speed;
        }
        if (boss->y > player->y) {
            boss->y = boss->y - boss->speed;
        }
    }

    uint32_t now = HAL_GetTick();

    if (boss->laser_warning_tick != 0 && now - boss->laser_warning_tick >= LASER_WARNING_MS) {
        boss->laser_fire_tick = now;
        boss->laser_warning_tick = 0;
    }

    if (boss->type == 1) {
        if (now - boss->last_attack_tick >= boss->attack_interval_ms) {
            int attack_type = rand() % 3;

            if (attack_type == 0) {
                Boss_Attack_1(boss, boss_bullets);
            }
            else if (attack_type == 1) {
                Boss_Attack_2(boss, boss_bullets);
            }
            else {
                Boss_Attack_3(boss);
            }
            boss->last_attack_tick = now;
        }
    }
    else {
        if (now - boss->last_attack_tick >= boss->attack_interval_ms) {
            Boss_Attack_4(boss, boss_bullets, player);
            boss->last_attack_tick = now;
        }
    }
}

void Boss_Draw(Boss_t* boss) {
    if (boss->type == 1) {
        LCD_Draw_Sprite_Scaled(boss->x, boss->y, 40, 40, (const uint8_t*)boss1_sprite, 2);
    }
    else {
        LCD_Draw_Sprite_Scaled(boss->x, boss->y, 40, 40, (const uint8_t*)boss2_sprite, 2);
    }

    // Draw warning line
    if (boss->laser_warning_tick != 0 && HAL_GetTick() - boss->laser_warning_tick < 500) {
        AABB warning_box = Boss_GetLaserAABB(boss);
        LCD_Draw_Rect(
            warning_box.x, 
            warning_box.y, 
            warning_box.width, 
            warning_box.height, 
            8, 
            1);
    }

    // Draw laser
    if (boss->laser_fire_tick != 0 && HAL_GetTick() - boss->laser_fire_tick < 500) {
        AABB laser = Boss_GetLaserAABB(boss);
        LCD_Draw_Rect(
            laser.x, 
            laser.y, 
            laser.width, 
            laser.height, 
            4, 
            1);
    }
}

AABB Boss_GetAABB(Boss_t* boss) {
    AABB box;
    box.x = boss->x;
    box.y = boss->y;
    box.width = boss->width;
    box.height = boss->height;
    return box;
}

AABB Boss_GetLaserAABB(Boss_t* boss) {
    AABB box;

    if (boss->laser_vertical) {
        box.x = SCREEN_WIDTH / 2 - LASER_THICKNESS / 2;
        box.y = 0;
        box.width = LASER_THICKNESS;
        box.height = SCREEN_HEIGHT;
    } else {
        box.x = 0;
        box.y = SCREEN_HEIGHT / 2 - LASER_THICKNESS / 2;
        box.width = SCREEN_WIDTH;
        box.height = LASER_THICKNESS;
    }

    return box;
}

void Boss_Attack_1(Boss_t* boss, BulletPool_t* pool) {
    BulletPool_Generate(pool, 4, 4, boss->x, boss->y + 20, -3.0f, 0.0f);
    BulletPool_Generate(pool, 4, 4, boss->x, boss->y + 50, -3.0f, 0.0f);
}

void Boss_Attack_2(Boss_t* boss, BulletPool_t* pool) {
    for (int i = 0; i < 4; i++) {
        int16_t rand_x = rand() % SCREEN_WIDTH;
        BulletPool_Generate(pool, 10, 12, rand_x, 0, 0.0f, 2.0f);
    }
}

void Boss_Attack_3(Boss_t* boss) {
    boss->laser_warning_tick = HAL_GetTick();
    boss->laser_fire_tick = 0;
    boss->laser_vertical = rand() % 2;
}

void Boss_Attack_4(Boss_t* boss, BulletPool_t* pool, Player_t* player) {
    float vx = 0.0f;
    float vy = 0.0f;

    if (player->x < boss->x) {
        vx = -3.0f;
    }

    if (player->x > boss->x) {
        vx =  3.0f;
    }
    if (player->y < boss->y) {
        vy = -1.5f;
    }
    if (player->y > boss->y) {
        vy =  1.5f;
    }

    BulletPool_Generate(
        pool,
        6, 6,
        boss->x + boss->width / 2,
        boss->y + boss->height / 2,
        vx, 
        vy
    );
}
